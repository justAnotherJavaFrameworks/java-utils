package de.slfw.delete;

public class WalkingCompleteResult {

	private Integer amountFiles;
	private Long completeFileSize;
	/**
	 * @return the amountFiles
	 */
	public Integer getAmountFiles() {
		return amountFiles;
	}
	/**
	 * @param amountFiles the amountFiles to set
	 */
	public void setAmountFiles(Integer amountFiles) {
		this.amountFiles = amountFiles;
	}
	/**
	 * @return the completeFileSize
	 */
	public Long getCompleteFileSize() {
		return completeFileSize;
	}
	/**
	 * @param completeFileSize the completeFileSize to set
	 */
	public void setCompleteFileSize(Long completeFileSize) {
		this.completeFileSize = completeFileSize;
	}
	
	
	
}
